package Print_Alphabet;

import java.util.Scanner;

/**
 *
 * @author LapHoang
 */
public class Main {
//    Bang chu cai

    public static void main(String[] args) {
        String a, b;
        Scanner scanner = new Scanner(System.in);
        a = scanner.nextLine();
        b = scanner.nextLine();
        for (char i = 'a'; i <= 'b'; i++) {
            System.out.printf("%s",i);
        }
    }
}
